/**
 * HTML DOM
 * 
 * Element: Có những cách để get được những node trong dom là element là ID, class
 * CSS selector, HTML Collection 
 * 
 */

// phương thức lấy id của element
document.getElementById('id'); // truyền vào id muốn đi tới

// phương thức lấy class của element
document.getElementsByClassName('className'); // truyền vào className muốn đi tới

// phương thức lấy tag của element
document.getElementsByTagName('tagName'); // truyền vào tagName muốn đi tới

// phương thức lấy bằng CSS selector của element
document.querySelector(''); // truyền vào class hoac tag muốn đi tới

// phương thức lấy bằng CSS selector all của element
document.querySelectorAll(''); // cung nhu tren nhung tra ra nhieu element